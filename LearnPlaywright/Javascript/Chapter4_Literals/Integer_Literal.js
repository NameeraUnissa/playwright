// Integer literal

let decimal = 10; //Decimal literal
console.log("Decimal:",decimal);
console.log("Type:",typeof decimal);


let binary = 0b1010; //Binary literal
console.log("Binary:",binary);
console.log("Type:",typeof binary);

let octal = 0o744; //Octal literal
console.log("Octal:",octal);
console.log("Type:",typeof octal);

let hexadecimal = 0xFF; //Hexadecimal literal
console.log("Hexadecimal:",hexadecimal);
console.log("Type:",typeof hexadecimal);