let float= 3.14; //Floating point literal
console.log(float);
console.log(typeof float);

let float2= 3.14e2; //Floating point literal with exponential notation
console.log(float2);
console.log(typeof float2);

let float3= 3.14e-2; //Floating point literal with negative exponential notation
console.log(float3);
console.log(typeof float3);

let float4= 3.14e+2; //Floating point literal with positive exponential notation
console.log(float4);
console.log(typeof float4);

let float5 =-5.67; //Negative floating point literal
console.log(float5);
console.log(typeof float5);

let float6 = 5. ; //Floating point literal with no decimal part
console.log(float6);
console.log(typeof float6);

//Exponential notation can also be used with negative numbers

let exp1 =1.5e2; //Exponential notation with positive exponent
console.log(exp1);
console.log(typeof exp1);

let exp2 = 1.5e-2; //Exponential notation with negative exponent
console.log(exp2);
console.log(typeof exp2);

let exp3 = 2E5; //Exponential notation with uppercase 
console.log(exp3);
console.log(typeof exp3);